    private void loadData() {
        model.setRowCount(0);
        List<DichVu> list = dao.getAll();
        for (DichVu dv : list) {
            model.addRow(new Object[]{dv.getMaDV(), dv.getTenDV(), dv.getDonGia(), dv.getDonViTinh()});
        }
    }

