    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String keyword = JOptionPane.showInputDialog(this, "Nhập tên dịch vụ cần tìm:");
        if (keyword != null && !keyword.trim().isEmpty()) {
            List<DichVu> list = dao.getAll();
            model.setRowCount(0);
            for (DichVu dv : list) {
                if (dv.getTenDV().toLowerCase().contains(keyword.toLowerCase())) {
                    model.addRow(new Object[]{dv.getMaDV(), dv.getTenDV(), dv.getDonGia(), dv.getDonViTinh()});
                }
            }
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

    private void tblDichVuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDichVuMouseClicked
        int row = tblDichVu.getSelectedRow();
        if (row >= 0) {
            txtMaDV.setText(model.getValueAt(row, 0).toString());
            txtTenDV.setText(model.getValueAt(row, 1).toString());
            txtDonGia.setText(model.getValueAt(row, 2).toString());
            cboDonVi.setSelectedItem(model.getValueAt(row, 3).toString());
        }
    }//GEN-LAST:event_tblDichVuMouseClicked


